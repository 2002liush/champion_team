//transmit temperature infomation to uart
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
		if(htim->Instance == TIM6)
		{
			f = count / 2.02;
			count = 0;
		}
		if(htim->Instance == TIM7)
		{
			val = HAL_GPIO_ReadPin(GPIOE,GPIO_PIN_9);
			count_2++;
			if(val == 1 && previous==0)
			{
				jduge++;
				previous = 1;
			}
			if(val == 0 && previous==1)
			{
				jduge++;
				previous = 0;
			}
			if(jduge==3)
			{
				f = 1000.0 / count_2 / 1.06;
				count_2 = 0;
				jduge=1;
			}
		}

}

